"""CLI: run the completion benchmark and (re)write ``benchmarks/RESULTS.md``.

    python -m benchmarks.run_benchmark

Runs NeuroDB ``complete`` against sklearn ``KNNImputer`` and ``IterativeImputer``
on the synthetic multimodal mixture and on UCI Wine, with the fair protocol in
:mod:`benchmarks.protocol`, and writes a Markdown results table plus the gate
verdict. The verdict is computed, not asserted here — see
``tests/test_benchmark_gate.py`` for the executable gate.
"""

from __future__ import annotations

import io
from pathlib import Path

from .datasets import Dataset, load_wine, make_multimodal
from .protocol import BenchmarkReport, run_benchmark

GATE_RATIO = 0.75  # NeuroDB must reach <= 0.75 x the best baseline to "win".

_RESULTS = Path(__file__).resolve().parent / "RESULTS.md"


def _ratio(rep: BenchmarkReport) -> float:
    best = min(rep.by_name("knn").overall_rmse, rep.by_name("iterative").overall_rmse)
    return rep.by_name("neurodb").overall_rmse / best


def _write_report(rep: BenchmarkReport, out: io.StringIO) -> None:
    out.write(f"### {rep.dataset}\n\n")
    out.write(
        f"rows: train={rep.n_train}, test={rep.n_test} - dimension={rep.d} - "
        f"modes={rep.n_modes} - masked per record={rep.mask_fraction:.0%}\n\n"
    )
    out.write("| method | tuned config | overall RMSE (std units) |\n")
    out.write("|---|---|---|\n")
    for name in ("neurodb", "knn", "iterative"):
        m = rep.by_name(name)
        out.write(f"| {m.method} | `{m.config}` | {m.overall_rmse:.4f} |\n")
    ratio = _ratio(rep)
    verdict = "**PASS**" if ratio <= GATE_RATIO else "**FAIL**"
    out.write(
        f"\nNeuroDB / best-baseline = **{ratio:.3f}** "
        f"(gate target <= {GATE_RATIO}; lower is better). Gate: {verdict}\n\n"
    )


def build_results() -> str:
    datasets: list[Dataset] = [
        make_multimodal(n_clusters=6, dim=12, per_cluster=200, separation=6.0, seed=0),
        load_wine(),
    ]
    out = io.StringIO()
    out.write("# Tier 0 completion benchmark — results\n\n")
    out.write(
        "_Generated by `python -m benchmarks.run_benchmark`._ "
        "NeuroDB `complete` vs scikit-learn `KNNImputer` / `IterativeImputer`, "
        "per-field RMSE on held-out masked records (standardized units). "
        "Every method tunes its hyperparameters on a validation split (equal "
        "budget); error is scored on masked cells only, in float64.\n\n"
    )
    any_pass = False
    for ds in datasets:
        rep = run_benchmark(ds, mask_fraction=0.4, seed=0)
        _write_report(rep, out)
        any_pass = any_pass or _ratio(rep) <= GATE_RATIO
    out.write("## Verdict\n\n")
    if any_pass:
        out.write("At least one dataset meets the gate. See per-dataset rows.\n")
    else:
        out.write(
            "**Gate not met on any dataset.** NeuroDB's dot-product–softmax "
            "completion is beaten by a distance-based `KNNImputer` everywhere, "
            "and the gap *widens* as the data becomes more cleanly multimodal "
            "(better-separated clusters let KNN snap within-cluster while the "
            "softmax keeps blending). See `docs/ACCURACY.md` for the full "
            "characterization and the repositioning this implies.\n"
        )
    return out.getvalue()


def main() -> None:
    text = build_results()
    _RESULTS.write_text(text, encoding="utf-8")
    print(text)
    print(f"\nwrote {_RESULTS}")


if __name__ == "__main__":
    main()
